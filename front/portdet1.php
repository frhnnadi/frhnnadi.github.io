                Pemerintah Kabupaten Garut, Jawa Barat, meraih penghargaan Anugerah Kebudayaan Indonesia dari Kementerian Pendidikan dan Kebudayaan Republik Indonesia sebagai apresiasi negara terhadap para pegiat seni, adat istiadat dan kebudayaan di Garut. "Penghargaan ini bukan untuk diri saya sebagai bupati, penghargaan ini merupakan bentuk penghormatan dari negara kepada seluruh warga Garut, terutama terhadap adat-istiadatnya, kebudayaannya, keseniannya, dan juga hal-hal lain termasuk dengan kekayaan alamnya," kata Bupati Garut Rudy Gunawan dalam acara penyerahan Anugerah Kebudayaan Indonesia 2020 di Gedung Pendopo Garut, Rabu. Ia menuturkan penghargaan dari Kemendikbud itu bentuk apresiasi negara kepada seluruh warga Garut terutama para pegiat seni, adat istiadat, dan kebudayaan dengan hasil penilaian objektif di lapangan oleh tim dari Kemendikbud. Penghargaan itu, kata Rudy, merupakan hasil dari kolaborasi dan sinergitas antara insan kebudayaan yang dikoordinasi oleh Dinas Pariwisata dan Kebudayaan (Disparbud) Kabupaten Garut dalam rangka memperkuat pariwisata Garut. "Dinas Pariwisata dan Kebudayaan yang mengolaborasi dan menyinergikan, supaya kebudayaan-kebudayaan, kesenian-kesenian, adat-istiadat, dan sebagainya diramu menjadi sesuatu yang bisa dihadirkan untuk memperkuat pariwisata Kabupaten Garut," katanya.
